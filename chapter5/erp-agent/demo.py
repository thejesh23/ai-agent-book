"""
Experiment 5-10: Command-line entry for an ERP Agent with natural language interaction (NL -> SQL, artifact mode).

Core idea (artifact mode): The Agent is only responsible for "generating SQL artifacts", not for moving data itself;
the actual query is executed by the system using the generated SQL against the database, and the result table is delivered directly to the user interface.

Subcommands:
  run     Online: Agent generates SQL -> execute -> compare with reference implementation (requires OPENAI_API_KEY, default subcommand)
  gold    Offline: Execute built-in "standard SQL" for 10 questions -> compare with reference implementation (no API needed, for self-check/demo)
  ask     Online: Single natural language query -> generate SQL -> execute and print result table (requires OPENAI_API_KEY)
  initdb  Create tables and load reproducible seed data into a SQLite file (offline, convenient for manual inspection with sqlite3)

When no subcommand is given, it is equivalent to `run`, maintaining the same default behavior as the old `python demo.py`.
Full usage see `python demo.py --help`, or for a subcommand `python demo.py <subcommand> --help`.
"""

